CREATE TABLE IF NOT EXISTS logs (
 date DATE NOT NULL,
 content TEXT NOT NULL
) PARTITION BY RANGE (date);


CREATE TABLE IF NOT EXISTS logs_q1_2022 PARTITION OF logs FOR VALUES FROM ('2022-01-01') TO ('2022-04-01');

CREATE TABLE IF NOT EXISTS logs_q2_2022 PARTITION OF logs FOR VALUES FROM ('2022-04-01') TO ('2022-07-01');

CREATE TABLE IF NOT EXISTS logs_q3_2022 PARTITION OF logs FOR VALUES FROM ('2022-07-01') TO ('2022-10-01');

CREATE TABLE IF NOT EXISTS logs_q4_2022 PARTITION OF logs FOR VALUES FROM ('2022-10-01') TO ('2023-01-01');


CREATE INDEX IF NOT EXISTS logs_q1_2022_data_idx ON logs_q1_2022(date);
